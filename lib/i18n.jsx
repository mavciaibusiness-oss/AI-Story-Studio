const I18nCtx = createContext(null);

export function detectLocale() {
  /* Varsayılan İngilizce — küresel pazar önce. Türkçe tarayıcı diliyle
     ya da kullanıcı seçimiyle gelir, seçim localStorage'da kalıcıdır. */
  if (typeof navigator === 'undefined') return 'en';
  const langs = navigator.languages || [navigator.language || 'en'];
  for (const l of langs) {
    const code = String(l).toLowerCase().slice(0, 2);
    if (DICT[code]) return code;
  }
  return 'en';
}

export function I18nProvider({ children, initial }) {
  const [locale, setLocaleState] = useState(initial || 'en');
  const [ready, setReady] = useState(false);

  /* Sıra: kayıtlı tercih → tarayıcı dili → en
     Hydration uyuşmazlığı olmasın diye ilk render sunucudakiyle aynı,
     tarayıcı tercihi mount sonrası uygulanır. */
  useEffect(() => {
    let saved = null;
    try { saved = localStorage.getItem('ui-locale'); } catch (e) {}
    const next = (saved && DICT[saved]) ? saved : detectLocale();
    setLocaleState(next);
    setReady(true);
  }, []);

  const setLocale = useCallback((k) => {
    if (!DICT[k]) return;
    setLocaleState(k);
    try { localStorage.setItem('ui-locale', k); } catch (e) {}
    if (typeof document !== 'undefined') document.documentElement.lang = k;
  }, []);

  useEffect(() => {
    if (typeof document !== 'undefined') document.documentElement.lang = locale;
  }, [locale]);

  /* t('key') ya da t('key', { n: 5 }) — {n} yer tutucuları değiştirilir */
  const t = useCallback((key, vars) => {
    let s = DICT[locale]?.[key] ?? DICT.en?.[key] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.split('{' + k + '}').join(String(v));
    return s;
  }, [locale]);

  return (
    <I18nCtx.Provider value={{ locale, setLocale, t, ready }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() {
  const c = useContext(I18nCtx);
  if (!c) throw new Error('useI18n yalnızca I18nProvider içinde kullanılır.');
  return c;
}

/* Kısa yol: const t = useT(); t('nav.script') */
export function useT() {
  return useI18n().t;
}
